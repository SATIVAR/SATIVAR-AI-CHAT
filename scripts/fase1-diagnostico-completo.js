// Fun√ß√£o para fazer requisi√ß√µes HTTP/HTTPS
function makeRequest(url, options = {}) {
  return new Promise((resolve, reject) => {
    const isHttps = url.startsWith('https://');
    const client = isHttps ? https : http;
    
    const requestOptions = {
      ...options,
      timeout: 15000,
      headers: {
        'User-Agent': 'SATIZAP-Diagnostico/1.0',
        'Content-Type': 'application/json',
        'Cache-Control': 'no-cache',
        ...options.headers
      }
    };

    const req = client.request(url, requestOptions, (res) => {
      let data = '';
      
      res.on('data', (chunk) => {
        data += chunk;
      });
      
      res.on('end', () => {
        try {
          const jsonData = data ? JSON.parse(data) : null;
          resolve({
            status: res.statusCode,
            statusText: res.statusMessage,
            headers: res.headers,
            data: jsonData,
            rawData: data
          });
        } catch (parseError) {
          resolve({
            status: res.statusCode,
            statusText: res.statusMessage,
            headers: res.headers,
            data: null,
            rawData: data,
            parseError: parseError.message
          });
        }
      });
    });

    req.on('error', (error) => {
      reject(error);
    });

    req.on('timeout', () => {
      req.destroy();
      reject(new Error('Request timeout'));
    });

    req.end();
  });
}

// Fun√ß√£o para buscar credenciais do banco
async function getCredentials() {
  const prisma = new PrismaClient();
  
  try {
    console.log(`üîç Buscando credenciais da associa√ß√£o: ${TEST_CONFIG.associationSubdomain}`);
    
    const association = await prisma.association.findFirst({
      where: {
        subdomain: TEST_CONFIG.associationSubdomain
      }
    });
    
    if (!association) {
      throw new Error(`Associa√ß√£o "${TEST_CONFIG.associationSubdomain}" n√£o encontrada`);
    }
    
    console.log(`‚úÖ Associa√ß√£o encontrada: ${association.name}`);
    
    // Tentar wordpressAuth primeiro
    if (association.wordpressAuth) {
      const auth = typeof association.wordpressAuth === 'string' 
        ? JSON.parse(association.wordpressAuth) 
        : association.wordpressAuth;
      
      if (auth.username && auth.password) {
        console.log('‚úÖ Usando credenciais wordpressAuth');
        return {
          username: auth.username,
          password: auth.password
        };
      }
    }
    
    // Tentar apiConfig
    if (association.apiConfig) {
      try {
        const { decryptApiConfig } = require('../src/lib/crypto');
        const apiConfig = await decryptApiConfig(association.apiConfig);
        
        if (apiConfig.credentials?.applicationPassword) {
          console.log('‚úÖ Usando credenciais apiConfig (Application Password)');
          return {
            username: apiConfig.credentials.applicationPassword.username,
            password: apiConfig.credentials.applicationPassword.password
          };
        }
      } catch (decryptError) {
        console.log('‚ö†Ô∏è Erro ao descriptografar apiConfig, tentando wordpressAuth...');
      }
    }
    
    throw new Error('Nenhuma credencial v√°lida encontrada');
    
  } finally {
    await prisma.$disconnect();
  }
}

// Fun√ß√£o para criar headers de autentica√ß√£o
function createAuthHeaders(username, password) {
  const credentials = Buffer.from(`${username}:${password}`).toString('base64');
  return {
    'Authorization': `Basic ${credentials}`
  };
}

